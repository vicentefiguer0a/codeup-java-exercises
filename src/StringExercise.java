public class StringExercise {

    public static void main(String[] args) {
        // We don't need no education
        // We don't need no thought control
        String str1 = "We don't need no education\nWe don't need no thought control";
        System.out.printf("%s%n", str1);

        // Check "this" out!, "s inside of "s!
        String str2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.printf("%s%n", str2);

        // In windows, the main drive is usually C:\
        String str3 = "In windows, the main drive is usually C:\\";
        System.out.printf("%s%n", str3);

        // I can do backslashes \, double backslashes \\,
        // and the amazing triple backslash \\\!
        String str4 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash\\\\\\!";
        System.out.printf("%s%n", str4);
    }
}